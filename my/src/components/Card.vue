<template>
  <div>
    <div class="container">
      <h1 class="question" id="title1">{{ title }}</h1>
      <v-text-field
        v-if="inputType == 'text'"
        class="field red--text"
        :value="value"
        @input="onChange($event)"
      ></v-text-field>
      <div v-else-if="inputType == 'text-area'">
      	<v-textarea
      	  :value="value"
      	  @input="onChange($event)"
      	  counter="500"
      	  auto-grow
      	  v-validate="{ required: true, max: 500}"
      	></v-textarea>
      	<!-- <v-progress-linear
      	            v-if="custom"
      	            slot="progress"
      	            :value="q1Progress"
      	            :color="q1Color"
      	            height="5"
      	          ></v-progress-linear> -->
      </div>
      <v-select
        v-else-if="inputType == 'single-select'"
        :items="selectData"
        prepend-icon="map"
        single-line
        :value="value"
        @input="onChange($event)"
      ></v-select>
      <div v-else-if="inputType == 'date'" class="date-row">
        <v-select
          v-for="(input, i) in dates"
          :key="i"
          :items="input.options"
          @input="onDate(input.label, $event)"
          :label="input.label"
          v-model="input.value"
        ></v-select>
      </div>
      <div v-else-if="inputType == 'radio-select'" class="radio-row">
        <span v-for="(data, i) in selectData" :key="i">
          <input type="radio" name="inputs" :id="data" :value="data" />
          <label :for="data">{{ data }}</label>
        </span>
      </div>
      <div v-else-if="inputType == 'combo-box'">
        <v-combobox
        :items="selectData"
        prepend-icon="map"
        single-line
        :value="value"
        @input="onChange($event)"
      ></v-combobox>
      </div>
      <div v-else-if="inputType == 'multi-select'">
        <v-select
        :items="selectData"
        prepend-icon="map"
        single-line
        multiple
        :value="value"
        @input="onChange($event)"
      ></v-select>
      </div>
    </div>
  </div>
</template>

<script lang="ts">
import Vue from 'vue';
import firebase from 'firebase';
import { months, years, days } from '../data';

export default Vue.extend({
  props: ['title', 'value', 'requestUpdate', 'inputType', 'selectData', 'textLimit'],
  data() {
    return {
      dates: [],
    } as { dates: any };
  },
  methods: {
    onChange(event) {
      this.$emit('input', event);
      this.requestUpdate();
    },
    onDate(type: string, value: any) {
      if (type.toLowerCase() === 'year') {
        this.$emit(
          'input',
          firebase.firestore.Timestamp.fromDate(
            new Date(
              value,
              months.indexOf(this.dates[1].value),
              this.dates[2].value,
            ),
          ),
        );
      } else if (type.toLowerCase() === 'month') {
        this.$emit(
          'input',
          firebase.firestore.Timestamp.fromDate(
            new Date(
              this.dates[0].value,
              months.indexOf(value),
              this.dates[2].value,
            ),
          ),
        );
      } else {
        this.$emit(
          'input',
          firebase.firestore.Timestamp.fromDate(
            new Date(
              this.dates[0].value,
              months.indexOf(this.dates[1].value),
              value,
            ),
          ),
        );
      }
      this.requestUpdate();
    },
    updateDates() {
      if (this.inputType === 'date') {
        this.dates = [
          {
            label: 'Year',
            options: years,
            value: new Date(this.value.toDate()).getFullYear(),
          },
          {
            label: 'Month',
            options: months,
            value: months[new Date(this.value.toDate()).getMonth()],
          },
          {
            label: 'Day',
            options: days,
            value: new Date(this.value.toDate()).getDate(),
          },
        ];
      }
    },
  },
  mounted() {
    this.updateDates();
  },
  watch: {
    value(newVal, oldVal) {
      this.updateDates();
    },
  },
  computed: {
    q1Progress() {
      return Math.min(100, this.application.responses.q1.length / 5);
    },
  },
});
</script>

